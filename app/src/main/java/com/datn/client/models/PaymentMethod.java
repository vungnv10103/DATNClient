package com.datn.client.models;

public class PaymentMethod extends _BaseModel {
    private int key;
    private String value;

    public PaymentMethod(int key, String value) {
        this.key = key;
        this.value = value;
    }

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
